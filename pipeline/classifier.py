"""
pipeline/classifier.py

Шаг 2 (логический) — Классификация документов.

Определяет тип каждого документа по ключевым словам из первых ~6K символов.
Типы: КОНТРАКТ, ТЗ, ИЗВЕЩЕНИЕ, ТРЕБОВАНИЯ, МУСОР, ПРОЧЕЕ
"""

DOC_TYPE_SIGNALS: dict[str, list[str]] = {
    "КОНТРАКТ": [
        "проект контракта",
        "государственный контракт",
        "муниципальный контракт",
        "договор поставки",
        "условия исполнения контракта",
        "исполнитель обязуется",
        "заказчик обязуется",
    ],
    "ТЗ": [
        "техническое задание",
        "технические характеристики",
        "технические требования",
        "описание объекта закупки",
        "спецификация товара",
        "функциональные характеристики",
    ],
    "ИЗВЕЩЕНИЕ": [
        "извещение о проведении",
        "начальная максимальная цена",
        "нмц контракта",
        "способ определения поставщика",
        "электронный аукцион",
        "запрос котировок",
        "конкурентная закупка",
    ],
    "ТРЕБОВАНИЯ": [
        "требования к участникам",
        "единые требования к участникам",
        "обеспечение заявки",
        "критерии оценки заявок",
        "документы для участия",
        "правомочность участника",
    ],
}

JUNK_SIGNALS: list[str] = [
    "форма заявки участника",
    "бланк заявки",
    "образец заявки",
    "заявка участника закупки",
    "форма для заполнения участником",
]


def classify(text: str) -> str:
    """
    Возвращает тип документа: КОНТРАКТ | ТЗ | ИЗВЕЩЕНИЕ | ТРЕБОВАНИЯ | МУСОР | ПРОЧЕЕ
    """
    sample = text[:6000].lower()

    for signal in JUNK_SIGNALS:
        if signal in sample:
            return "МУСОР"

    scores: dict[str, int] = {}
    for doc_type, signals in DOC_TYPE_SIGNALS.items():
        scores[doc_type] = sum(1 for s in signals if s in sample)

    best_type, best_score = max(scores.items(), key=lambda x: x[1])
    return best_type if best_score > 0 else "ПРОЧЕЕ"


# Приоритет типов при сборке контекста (чем меньше — тем важнее)
TYPE_PRIORITY: dict[str, int] = {
    "КОНТРАКТ":   1,
    "ТЗ":         2,
    "ИЗВЕЩЕНИЕ":  3,
    "ТРЕБОВАНИЯ": 4,
    "ПРОЧЕЕ":     5,
    "МУСОР":      99,
}
